package business.enums;

/**
 * Enumerado que define las franjas horarias disponibles para actividades en campamentos.
 * Puede ser por la mañana (MORNING) o por la tarde (AFTERNOON).
 */
public enum TimeSlot {
    /**
     * Representa la franja horaria de la mañana.
     */
    MORNING,

    /**
     * Representa la franja horaria de la tarde.
     */
    AFTERNOON
}
